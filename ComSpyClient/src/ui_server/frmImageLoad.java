    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        final JFileChooser fc = new JFileChooser();
        
        int rev = fc.showDialog(this, local.getString("btnSavehere"));
        if(rev == JFileChooser.APPROVE_OPTION){
            File dd = new File(Filename);
            File f = fc.getCurrentDirectory();
            String NewPath = f.getAbsoluteFile() + File.separator + dd.getName();
            File nn = new File(NewPath);
            if(dd.renameTo(nn)){
                JOptionPane.showMessageDialog(this, local.getString("msg1") + " " + NewPath, local.getString("btnSave"),JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, local.getString("msg2") + " " + dd.getAbsolutePath(),local.getString("btnSave"),JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

